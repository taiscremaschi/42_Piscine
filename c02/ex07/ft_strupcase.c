char	*ft_strupcase(char *str)
{
	int	cont;

	cont = 0;
	while (str[cont] != '\0')
	{
		if (str[cont] >= 97 && str[cont] <= 122)
		{
			str[cont] -= 32;
		}
		else
		{
			cont++;
		}
	}
	return (str);
}
