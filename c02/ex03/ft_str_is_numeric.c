int	ft_str_is_numeric(char *str)
{
	int	cont;

	cont = 0;
	if (str[cont] == '\0')
	{
		return (1);
	}
	while (str[cont] != '\0')
	{
		if (str[cont] >= '0' && str[cont] <= '9')
		{
			cont++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
